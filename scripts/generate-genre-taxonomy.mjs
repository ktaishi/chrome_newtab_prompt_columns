function parseOpenAiGenreClassification(raw) {
  const text = String(raw || "").trim();
  const jsonMatch = text.match(/\\{[\\s\\S]*\\}/);
  if (!jsonMatch) return { ...OPENAI_GENRE_FALLBACK, confidence: 0, reason: "" };
  try {
    const parsed = JSON.parse(jsonMatch[0]);
    const genreId = String(parsed.genreId || parsed.genre || "").trim();
    const subgenreId = String(parsed.subgenreId || parsed.subgenre || "").trim();
    if (getOpenAiSubgenreEntry(genreId, subgenreId)) {
      return {
        genreId,
        subgenreId,
        confidence: Number(parsed.confidence) || 0,
        reason: String(parsed.reason || "").trim()
      };
    }
  } catch (_) {
    /* fall through */
  }
  return { ...OPENAI_GENRE_FALLBACK, confidence: 0, reason: "" };
}
