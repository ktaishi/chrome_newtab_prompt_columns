function normalizeGenres(raw) {
  return raw.map((g) => ({
    id: g.id,
    label: g.label,
    description: g.description,
    subgenres: g.subgenres.map(([id, label, scene, sections, meta = {}]) => {
      const optimized = applySubgenreOptimization(id, sections, meta);
      return {
        id,
        label,
        scene,
        compact: optimized.compact,
        sections: optimized.sections.map(([title, instruction]) => ({ title, instruction })),
        compactSections: optimized.compactSections
          ? optimized.compactSections.map(([title, instruction]) => ({ title, instruction }))
          : null
      };
    })
  }));
}

